-- Bảng Lịch hẹn
CREATE TABLE APPOINTMENTS (
    appointment_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id NUMBER REFERENCES CUSTOMERS(customer_id),
    staff_id NUMBER REFERENCES STAFFS(staff_id), -- Thợ yêu cầu
    appointment_time TIMESTAMP NOT NULL,
    status NVARCHAR2(50) CHECK (status IN (N'Chờ xác nhận', N'Đã xác nhận', N'Đã đến', N'Đã hủy')),
    note NVARCHAR2(500),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Bảng Hóa đơn (Chính)
CREATE TABLE INVOICES (
    invoice_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    invoice_code VARCHAR2(30) UNIQUE NOT NULL, -- Ví dụ: HD20231027001
    customer_id NUMBER REFERENCES CUSTOMERS(customer_id),
    cashier_id NUMBER REFERENCES STAFFS(staff_id), -- Người thu ngân
    total_raw_amount NUMBER(15, 2), -- Tổng tiền trước giảm giá
    discount_amount NUMBER(15, 2) DEFAULT 0,
    final_amount NUMBER(15, 2), -- Số tiền thực thu
    payment_method NVARCHAR2(50), -- Tiền mặt, Chuyển khoản, Ví điện tử, Quẹt thẻ
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Bảng Chi tiết hóa đơn (Dịch vụ & Sản phẩm đã dùng)
CREATE TABLE INVOICE_DETAILS (
    detail_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    invoice_id NUMBER REFERENCES INVOICES(invoice_id),
    item_id NUMBER, -- ID của Service hoặc Product
    item_type VARCHAR2(20) CHECK (item_type IN ('SERVICE', 'PRODUCT')),
    quantity NUMBER DEFAULT 1,
    unit_price NUMBER(15, 2),
    staff_id NUMBER REFERENCES STAFFS(staff_id), -- Thợ thực hiện trực tiếp để tính hoa hồng
    commission_value NUMBER(15, 2) DEFAULT 0 -- Số tiền hoa hồng thợ nhận được từ mục này
);

