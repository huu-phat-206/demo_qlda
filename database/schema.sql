-- Bảng Danh mục (Để phân loại Cắt, Uốn, Nhuộm hoặc Mỹ phẩm bán lẻ)
CREATE TABLE CATEGORIES (
    category_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_name NVARCHAR2(100) NOT NULL,
    category_type VARCHAR2(20) CHECK (category_type IN ('SERVICE', 'PRODUCT'))
);

-- Bảng Dịch vụ (Menu của Salon)
CREATE TABLE SERVICES (
    service_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_id NUMBER REFERENCES CATEGORIES(category_id),
    service_name NVARCHAR2(200) NOT NULL,
    base_price NUMBER(15, 2) NOT NULL, -- Giá gốc
    duration_minutes NUMBER, -- Thời gian thực hiện ước tính
    description NVARCHAR2(500),
    is_active NUMBER(1) DEFAULT 1
);

-- Bảng Sản phẩm (Mỹ phẩm, thuốc nhuộm, dầu gội...)
CREATE TABLE PRODUCTS (
    product_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_id NUMBER REFERENCES CATEGORIES(category_id),
    product_name NVARCHAR2(200) NOT NULL,
    unit NVARCHAR2(50), -- Chai, Tuýp, Bộ
    selling_price NUMBER(15, 2), -- Giá bán lẻ cho khách
    stock_quantity NUMBER DEFAULT 0, -- Tồn kho hiện tại
    min_stock_level NUMBER DEFAULT 5, -- Mức tối thiểu để hệ thống cảnh báo
    is_active NUMBER(1) DEFAULT 1
);

-- Bảng Voucher & Khuyến mãi
CREATE TABLE VOUCHERS (
    voucher_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    voucher_code VARCHAR2(50) UNIQUE NOT NULL,
    discount_value NUMBER(15, 2), -- Số tiền giảm hoặc %
    discount_type VARCHAR2(10) CHECK (discount_type IN ('AMOUNT', 'PERCENT')),
    start_date DATE,
    end_date DATE,
    min_order_value NUMBER(15, 2) DEFAULT 0
);

