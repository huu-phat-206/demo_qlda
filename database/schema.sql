-- Sổ Quỹ Thu - Chi (Dùng cho báo cáo thuế hộ kinh doanh)
CREATE TABLE CASHBOOK (
    entry_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    transaction_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    entry_type VARCHAR2(10) CHECK (entry_type IN ('THU', 'CHI')),
    category NVARCHAR2(100), -- Doanh thu bán lẻ, Chi lương, Chi tiền điện, Chi nhập hàng...
    amount NUMBER(15, 2) NOT NULL,
    payment_method NVARCHAR2(50),
    reference_id NUMBER, -- ID của Invoice hoặc Inventory_Receipt nếu có
    note NVARCHAR2(500)
);
