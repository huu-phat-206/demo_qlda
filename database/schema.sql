-- Nhà cung cấp
CREATE TABLE SUPPLIERS (
    supplier_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    supplier_name NVARCHAR2(200) NOT NULL,
    phone VARCHAR2(15),
    address NVARCHAR2(255)
);

-- Phiếu nhập kho
CREATE TABLE INVENTORY_RECEIPTS (
    receipt_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    supplier_id NUMBER REFERENCES SUPPLIERS(supplier_id),
    import_date DATE DEFAULT SYSDATE,
    total_value NUMBER(15, 2),
    note NVARCHAR2(500)
);

-- Chi tiết nhập kho
CREATE TABLE INVENTORY_DETAILS (
    inv_detail_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    receipt_id NUMBER REFERENCES INVENTORY_RECEIPTS(receipt_id),
    product_id NUMBER REFERENCES PRODUCTS(product_id),
    quantity NUMBER NOT NULL,
    import_price NUMBER(15, 2),
    expiry_date DATE -- Hạn sử dụng (quan trọng cho hóa chất nhuộm/uốn)
);

