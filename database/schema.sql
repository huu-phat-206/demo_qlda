-- Bảng Khách hàng
CREATE TABLE CUSTOMERS (
    customer_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_code VARCHAR2(20) UNIQUE NOT NULL, -- Ví dụ: KH0001
    full_name NVARCHAR2(100) NOT NULL,
    phone_number VARCHAR2(15) UNIQUE NOT NULL,
    email VARCHAR2(100),
    birthday DATE,
    address NVARCHAR2(255),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Bảng Nhân viên
CREATE TABLE STAFFS (
    staff_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    staff_code VARCHAR2(20) UNIQUE NOT NULL, -- Ví dụ: NV001
    full_name NVARCHAR2(100) NOT NULL,
    phone_number VARCHAR2(15),
    role VARCHAR2(50), -- Stylist, Cashier, Manager
    base_salary NUMBER(15, 2) DEFAULT 0,
    is_active NUMBER(1) DEFAULT 1, -- 1: Đang làm việc, 0: Đã nghỉ
    created_at DATE DEFAULT SYSDATE
);

-- Bảng Chấm công & Ca làm việc
CREATE TABLE WORK_SHIFTS (
    shift_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    staff_id NUMBER REFERENCES STAFFS(staff_id),
    check_in TIMESTAMP,
    check_out TIMESTAMP,
    shift_date DATE DEFAULT TRUNC(SYSDATE),
    note NVARCHAR2(255)
);

